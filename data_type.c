// program to declare and initialize variables of all basic data types
#include<stdio.h>
int main()
{
    int a=10;
    float pi=3.14159;
    char ch='J';
    double d=9.345600;
    printf("int=%d\n",a);
    printf("float=%f\n",pi);
    printf("char=%c\n",ch);
    printf("double=%lf\n",d);
    return 0;
}